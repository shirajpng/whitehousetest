# Loops
# For:
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    if x == "banana":
        continue
        
    print(x)

# While
count = 0
while (count < 3): 
    count = count + 1
    print("Hello Nepal")

# Conditionals: if-elseif-else
i = 20
if (i == 10): 
    print("i is 10") 
elif (i == 15): 
    print("i is 15") 
elif (i == 20): 
    print("i is 20") 
else: 
    print("i is not present")
